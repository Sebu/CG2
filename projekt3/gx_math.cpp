void reduceData(VertList t, VertList &v, std::vector<GX_FACE> &f)
{
	std::vector< GX_VEC3 > c; // cache
	long max = t.size();
	long index = 0;
	
	cout << "Berechne Dreiecksreduktion" << endl;
	for (long i=0; i<max; i += 3)
    {
        int fv[3];
        
        
        for(int j=0; j<3; j++)
        {
            std::vector<GX_VEC3>::iterator result = find(c.begin(), c.end(), t[i+j]);
            if(result != c.end()) fv[j] = result->num;
            else {
                cout << "blub";
                t[i+j].num = index;
                c.push_back(t[i+j]);
                v.push_back(t[i+j]);
                fv[j] = index;
                index++;
            } 
        }
        GX_FACE face;
        face.v1 = fv[0]; face.v2 = fv[1]; face.v3 = fv[2];
        f.push_back(face);
    }
}

Point *genPointSquare(float posx, float posy ,float posz, gx_model& o)
{

    Matrix f;
    
    VertPointerList plist;


    ColumnVector b(10);
    b(1) = 1.0f; 
    b(2) = posx;      b(3) = posy;
    b(4) = posz;      b(5) = posx*posy; b(6) = posx*posz; 
    b(7) = posy*posz;  b(8) = posx*posx; b(9) = posy*posy;
    b(10) = posz*posz;

               
  Point *xp = new Point(posx,posy,posz); 
                    
    float h = 0.0f;
    plist = o.collectKNearest(*xp, 80);
    h = dist(*xp,*plist.back());

                
    Matrix part1(10,10); part1 = 0.0;
    Matrix part2(10,1); part2 = 0.0;
    
    for(int i=0; i < static_cast<int>(plist.size()); i++){
        float x = plist.at(i)->x;
        float y = plist.at(i)->y;
        float z = plist.at(i)->z;
        float fi = plist.at(i)->fi;

        ColumnVector bi(10);
        bi(1) = 1.0f;  
        bi(2) = x;      bi(3) = y;
        bi(4) = z;      bi(5) = x*y;    bi(6) = x*z;
        bi(7) = y*z;    bi(8) = x*x;    bi(9) = y*y;
        bi(10) = z*z;
                
        float d = dist(*xp,*plist.at(i));
        float wend = 0.0f;
                    
        d = d/h;
        wend = (1.0f - d)*(1.0f - d)*(1.0f - d)*(1.0f - d)*(4.0f*d+1.0f);

                    
        part1 += wend * bi * bi.t();
                
        part2 += wend * bi * fi;
    }
                
    f = b.t() * (part1.i() * part2);
    //  f(x) = b^t(x) * c(x)
     
    xp->fi = f(1, 1);
    
    return xp;
} 

